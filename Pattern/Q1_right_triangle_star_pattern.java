package Pattern;

import java.util.Scanner;

public class Q1_right_triangle_star_pattern {

    // Program to print right triangle star pattern

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n=sc.nextInt();
        for (int i = 0; i < n; i++) {
            System.out.print("* ");
            
        }
    }

}
